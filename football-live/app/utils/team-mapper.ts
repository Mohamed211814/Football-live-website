export function getTeamLogo(name: string, fallbackUrl?: string): string | undefined {
  // If the fallback URL is a broken placeholder logo, ignore it to use our official fallback flag
  let validFallback = fallbackUrl;
  if (fallbackUrl && (
    fallbackUrl.includes('image-not-available') || 
    fallbackUrl.includes('placeholder') || 
    fallbackUrl.includes('not-available') ||
    fallbackUrl.includes('broken')
  )) {
    validFallback = undefined;
  }

  if (validFallback) return validFallback;
  if (!name) return undefined;
  
  const clean = name.trim();
  if (clean in TEAM_LOGO_MAP) {
    return TEAM_LOGO_MAP[clean];
  }
  
  // Try clean base name (remove U17, U19, under 17, under 19, under 21, etc.)
  let base = clean;
  base = base.replace(/\sU\d{2}$/i, '').replace(/\sunder\s\d{2}$/i, '').replace(/\sتحت\s\d{2}$/i, '').trim();
  
  if (base in TEAM_LOGO_MAP) {
    return TEAM_LOGO_MAP[base];
  }
  
  // Try case-insensitive lookup
  const lower = clean.toLowerCase();
  const lowerBase = base.toLowerCase();
  for (const [key, val] of Object.entries(TEAM_LOGO_MAP)) {
    const keyLower = key.toLowerCase();
    if (keyLower === lower || keyLower === lowerBase) {
      return val;
    }
  }
  
  return undefined;
}
